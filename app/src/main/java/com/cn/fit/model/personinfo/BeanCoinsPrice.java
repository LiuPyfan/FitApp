package com.cn.fit.model.personinfo;

public class BeanCoinsPrice {

    private int count;
    private String detail;
    private long id;
    private float now_price;
    private float ori_price;

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public float getNow_price() {
        return now_price;
    }

    public void setNow_price(float now_price) {
        this.now_price = now_price;
    }

    public float getOri_price() {
        return ori_price;
    }

    public void setOri_price(float ori_price) {
        this.ori_price = ori_price;
    }


}
